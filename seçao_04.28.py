"""
Soma de três valores ao quadrado
"""

primeiro_valor = int(input("Digite o primeiro valor: "))

segundo_valor = int(input("Digite o segundo valor: "))

terceiro_valor = int(input("Digite o terceiro valor: "))

soma = int((primeiro_valor ** 2) + (segundo_valor ** 2) + (terceiro_valor ** 2))

print(f"A soma total de todos os valores digitados elevados ao quadrado é {soma}")